    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String cedula=txtCedulaBuscar.getText();
        Usuario estudiante = new Usuario();
        //validar que la cedula contega solo numeros 
        if(!validarSoloNumeros(cedula.trim())||txtCedulaBuscar.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Ingrese un número cédula válido: Debe estar compuesta por 10 dígitos numéricos");
            return;
        }
        //Traer datos del estudiante 
        estudiante=usuarioController.UsuarioPorCedula(cedula);
        String nombre = estudiante.getNombre()+" "+estudiante.getApellido();
        llenarTablaAmbientes(cedula,nombre);
    }//GEN-LAST:event_btnBuscarActionPerformed
private boolean validarSoloNumeros(String cadena)
{
    Pattern pattern = Pattern.compile("[0-9]{10}");
    Matcher matcher=pattern.matcher(cadena);
    return matcher.find();
}
