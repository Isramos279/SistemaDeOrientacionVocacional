private void llenarTablaAmbientes(String cedula,String nombre)
{
    limpiarTabla();
    lblNombreEstudiante.setText(nombre);
    ArrayList<Object[]> resultadoConsulta = usuarioController.ResultadoPorCedula(cedula);
    for(int i=0;i<6;i++)
    {
        modeloTablaAmbientes.addRow(resultadoConsulta.get(i));
    }
}
private void darPrivilegiosSegunNivel(Usuario usuario)
{
    if(usuario.getNivel() ==3)
    {
        txtCedulaBuscar.setVisible(false);
        btnBuscar.setVisible(false);
        lblCedula.setVisible(false);
    }
}
private void limpiarTabla()
{
    modeloTablaAmbientes.getDataVector().removeAllElements();
    tableAmbientes.updateUI();
}
